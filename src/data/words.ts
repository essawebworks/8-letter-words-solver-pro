// Dynamically calculate Scrabble points for any word
export function getScrabbleScore(word: string): number {
  const scores: { [key: string]: number } = {
    a: 1, e: 1, i: 1, o: 1, u: 1, l: 1, n: 1, s: 1, t: 1, r: 1,
    d: 2, g: 2,
    b: 3, c: 3, m: 3, p: 3,
    f: 4, h: 4, v: 4, w: 4, y: 4,
    k: 5,
    j: 8, x: 8,
    q: 10, z: 10
  };
  return word.toLowerCase().split('').reduce((acc, char) => acc + (scores[char] || 0), 0);
}

// Check if word contains any duplicate letters
export function hasDuplicateLetters(word: string): boolean {
  const chars = word.toLowerCase().split('');
  return new Set(chars).size !== chars.length;
}

// Count vowels in a word
export function countVowels(word: string): number {
  const vowels = ['a', 'e', 'i', 'o', 'u'];
  return word.toLowerCase().split('').filter(char => vowels.includes(char)).length;
}

